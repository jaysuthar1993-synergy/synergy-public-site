def get_transcript(video_id, max_chars=5000):
    try:
        from youtube_transcript_api import YouTubeTranscriptApi, TranscriptsDisabled, NoTranscriptFound
    except ImportError:
        print("ERROR: Run: pip install youtube-transcript-api")
        sys.exit(1)

    try:
        # Try English first, then Hindi auto-captions (common for Tally tutorial channels)
        try:
            transcript = YouTubeTranscriptApi.get_transcript(video_id, languages=['en'])
        except Exception:
            try:
                transcript_list = YouTubeTranscriptApi.list_transcripts(video_id)
                transcript = transcript_list.find_generated_transcript(['en', 'hi']).fetch()
            except Exception:
                return None

        text = ' '.join(t['text'] for t in transcript)
        # Clean auto-generated artifacts
        text = re.sub(r'\[.*?\]', '', text)
        text = re.sub(r'\s+', ' ', text).strip()
        return text[:max_chars]

    except Exception:
        return None


def get_content_via_gemini_video(video_id, video_title):
    """
    Gemini reads the YouTube video directly — no captions or download needed.
    Handles Hindi-only videos. Free tier: 8 hrs of video/day.
    Uses google-genai SDK (newer, supports from_uri for YouTube URLs).
    """
    try:
        from google import genai
        from google.genai import types
    except ImportError:
        return None

    if not GEMINI_API_KEY:
        return None

    video_url = f"https://www.youtube.com/watch?v={video_id}"
    client = genai.Client(api_key=GEMINI_API_KEY)

    for attempt in range(3):
        try:
            response = client.models.generate_content(
                model='gemini-2.5-flash-lite',
                contents=[
                    types.Part.from_uri(
                        file_uri=video_url,
                        mime_type='video/mp4',
                    ),
                    (
                        f"Video title: {video_title}\n"
                        "This is a Tally accounting tutorial for Indian CAs/accountants (may be in Hindi). "
                        "Extract all educational content and write it in clear English: the main topic, "
                        "every step of the process shown (with exact Tally menu paths and field names if "
                        "visible), shortcuts and tips mentioned, common mistakes discussed, and any Indian "
                        "accounting context (GST, ledger types, voucher types, etc.). "
                        "Write 400-600 words. Be specific — name exact buttons, menu items, and fields."
                    )
                ]
            )
            return response.text.strip()
        except Exception as e:
            err = str(e)
            if ('429' in err or '503' in err) and attempt < 2:
                wait = 30 + attempt * 30
                print(f"    Gemini video error ({err[:60]}) — retrying in {wait}s ({attempt+1}/3)...")
                time.sleep(wait)
            else:
                print(f"    Gemini video analysis error: {e}")
                return None


def get_audio_transcript_gemini(video_id, video_title):
    """
    Download audio-only with yt-dlp → upload to Gemini Files API → extract key points.
    Avoids the 10,800-frame limit that blocks long videos in direct video analysis.
    No FFmpeg needed — downloads native webm/m4a format from YouTube.
    Requires: pip install yt-dlp
    """
    try:
        import yt_dlp
    except ImportError:
        print('    Audio fallback skipped — run: pip install yt-dlp')
        return None

    try:
        from google import genai
        from google.genai import types
    except ImportError:
        return None

    if not GEMINI_API_KEY:
        return None

    import tempfile
    import glob as _glob

    url = f'https://www.youtube.com/watch?v={video_id}'

    with tempfile.TemporaryDirectory() as tmpdir:
        out_template = os.path.join(tmpdir, f'{video_id}.%(ext)s')

        base_opts = {
            'format': 'bestaudio[ext=webm]/bestaudio[ext=m4a]/bestaudio',
            'outtmpl': out_template,
            'quiet': True,
            'no_warnings': True,
            'noprogress': True,
        }

        print('    Downloading audio...')

        # Attempt 1: direct (works for most videos)
        try:
            with yt_dlp.YoutubeDL(base_opts) as ydl:
                ydl.download([url])
        except Exception:
            pass

        matches = _glob.glob(os.path.join(tmpdir, f'{video_id}.*'))

        # Attempt 2: retry with Chrome cookies (bypasses 403 on protected videos)
        if not matches:
            print('    Blocked — retrying with Chrome cookies...')
            try:
                cookie_opts = {**base_opts, 'cookiesfrombrowser': ('chrome',)}
                with yt_dlp.YoutubeDL(cookie_opts) as ydl:
                    ydl.download([url])
                matches = _glob.glob(os.path.join(tmpdir, f'{video_id}.*'))
            except Exception as e:
                print(f'    Audio download failed: {e}')
                return None

        if not matches:
            print('    Audio file not found after download')
            return None

        audio_path = matches[0]
        ext = audio_path.rsplit('.', 1)[-1].lower()
        size_mb = os.path.getsize(audio_path) / (1024 * 1024)
        print(f'    Audio: {size_mb:.1f} MB ({ext})')

        mime_map = {
            'webm': 'audio/webm',
            'm4a': 'audio/mp4',
            'mp4': 'audio/mp4',
            'mp3': 'audio/mp3',
            'ogg': 'audio/ogg',
            'opus': 'audio/webm',
            'wav': 'audio/wav',
        }
        mime_type = mime_map.get(ext, 'audio/webm')

        client = genai.Client(api_key=GEMINI_API_KEY)

        try:
            print('    Uploading to Gemini...')
            uploaded = client.files.upload(
                file=audio_path,
                config=types.UploadFileConfig(mime_type=mime_type),
            )

            # Wait for Gemini to finish processing the audio file (usually <10s)
            for _ in range(20):
                file_info = client.files.get(name=uploaded.name)
                if file_info.state.name == 'ACTIVE':
                    break
                if file_info.state.name == 'FAILED':
                    print('    Gemini file processing failed')
                    return None
                time.sleep(3)

            prompt = (
                f'Video title: "{video_title}"\n'
                'This is audio from a YouTube Tally/accounting tutorial for Indian CAs '
                '(may be in Hindi or English). '
                'Extract all useful educational content: the main topic, every step of the '
                'process demonstrated (with exact Tally menu paths and field names if mentioned), '
                'shortcuts and tips, common mistakes discussed, and any Indian accounting context '
                '(GST, TDS, voucher types, ledger names, etc.). '
                'Write 400-600 words of structured notes in English. Be specific — '
                'name exact buttons, menu items, and fields.'
            )

            result = None
            for attempt in range(3):
                try:
                    resp = client.models.generate_content(
                        model=GEMINI_MODEL,
                        contents=[uploaded, prompt],
                    )
                    result = resp.text.strip()
                    break
                except Exception as e:
                    err = str(e)
                    if ('429' in err or '503' in err) and attempt < 2:
                        wait = 30 + attempt * 30
                        print(f'    Gemini busy ({err[:50]}) — retrying in {wait}s...')
                        time.sleep(wait)
                    else:
                        print(f'    Gemini audio error: {e}')
                        break

            # Clean up the uploaded file from Gemini storage
            try:
                client.files.delete(name=uploaded.name)
            except Exception:
                pass

            return result

        except Exception as e:
            print(f'    Gemini audio error: {e}')
            return None


