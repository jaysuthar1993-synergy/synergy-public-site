def _sanitize_js_quotes(text):
    """Replace AI-generated curly/smart quotes with straight ASCII quotes."""
    return (text
        .replace('‘', "'").replace('’', "'")   # '' → ''
        .replace('“', '"').replace('”', '"')   # "" → ""
    )


def publish_article(article_js, commit=True, push=False):
    """Write article to all files. commit=False skips git (GitHub Actions commits separately)."""
    article_js = _sanitize_js_quotes(article_js)
    slug_match = re.search(r'''["']?slug["']?\s*:\s*["']([^"']+)["']''', article_js)
    if not slug_match:
        print('ERROR: Cannot find slug in article. Save as draft and check manually.')
        return False

    slug = slug_match.group(1)
    print(f'\nWriting /blog/{slug} to files...')

    # ── blogData.js ──
    blog_data = BLOG_DATA_FILE.read_text(encoding='utf-8')

    # Duplicate check — never publish the same slug twice
    if f"slug: '{slug}'" in blog_data or f'"slug": "{slug}"' in blog_data:
        print(f'  SKIP: slug "{slug}" already exists in blogData.js — article not duplicated.')
        return False

    # Find the closing ]; of the array (file may have functions after it)
    close_idx = blog_data.rfind('\n];')
    if close_idx == -1:
        print('  ERROR: blogData.js format unexpected — cannot find closing ];')
        return False
    chunk_before = blog_data[:close_idx].rstrip()
    if not chunk_before.endswith(','):
        chunk_before += ','
    chunk_after = blog_data[close_idx + 1:]  # keeps ]; and everything after (exports, functions)
    new_blog_data = chunk_before + f'\n  {article_js}\n' + chunk_after
    BLOG_DATA_FILE.write_text(new_blog_data, encoding='utf-8')
    print('  ✓ blogData.js updated')

    # sitemap.js auto-generates from blogData.js — no manual update needed

    if not commit:
        print(f'  ✓ Files written. Slug: {slug}')
        # Write slug to a file so the GitHub Actions step can read it
        (REPO_ROOT / '.seo_last_slug').write_text(slug, encoding='utf-8')
        return True

    # ── git commit (local use only) ──
    files = [
        str(BLOG_DATA_FILE),
    ]
    subprocess.run(['git', 'add'] + files, cwd=REPO_ROOT, check=True)
    commit_msg = f'feat(seo): add {slug} article via YouTube pipeline'
    result = subprocess.run(
        ['git', 'commit', '-m', commit_msg],
        cwd=REPO_ROOT, capture_output=True, text=True
    )
    if result.returncode == 0:
        print(f'  ✓ Committed: {commit_msg}')
    else:
        print(f'  WARNING: git commit issue: {result.stderr}')

    print(f'\n✅ Done! /blog/{slug} is ready.')

    if push:
        branch_result = subprocess.run(
            ['git', 'rev-parse', '--abbrev-ref', 'HEAD'],
            cwd=REPO_ROOT, capture_output=True, text=True
        )
        branch = branch_result.stdout.strip() or 'develop'
        push_result = subprocess.run(
            ['git', 'push', 'origin', branch],
            cwd=REPO_ROOT, capture_output=True, text=True
        )
        if push_result.returncode == 0:
            print(f'  ✓ Pushed to origin/{branch}')
            print(f'  Preview: https://develop.synergy-public-site.pages.dev/blog/{slug}')
            # Send Telegram approval request (non-blocking — if no token, just prints instructions)
            _send_telegram_approval(slug, article_js)
        else:
            print(f'  WARNING: git push failed: {push_result.stderr}')
            print(f'  Run manually: git push origin {branch}')
    else:
        print('Next step: git push origin develop')
        print('Then merge develop → master to go live on synergyfuturecorp.com')
    return True


