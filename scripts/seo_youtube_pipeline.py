def run_pipeline(topic, provider='gemini', auto=False, push=False):
    """
    auto=True  — no interactive prompt, just write files (used by GitHub Actions).
                 Caller is responsible for committing/PR after this returns True.
    auto=False — interactive y/n/s/r approval (local use).
    """
    print(f'\nSearching YouTube: "{topic} tally india" ...')
    videos = search_videos(topic, max_results=6)

    if not videos:
        print('No videos found. Try a different topic.')
        return False

    print(f'Found {len(videos)} videos. Fetching transcripts...')
    for v in videos:
        v['transcript'] = get_transcript(v['id'])
        icon = '✓' if v['transcript'] else '✗'
        print(f'  {icon} {v["title"][:65]}')

    usable = [v for v in videos if v['transcript']]
    no_caption = [v for v in videos if not v.get('transcript')]
    if no_caption:
        # Stage 3: Audio download → Gemini Files API (primary method, any video length)
        label = 'No captions found.' if not usable else 'Enriching with audio...'
        print(f'{label} Downloading audio for Gemini analysis...')
        for v in no_caption[:6]:  # Up to 6 videos — skip 403-blocked ones, get richer research
            print(f'  {v["title"][:65]}')
            content = get_audio_transcript_gemini(v['id'], v['title'])
            if content:
                v['transcript'] = content
                print(f'    ✓ {len(content)} chars extracted from audio')
            else:
                if not v.get('transcript'):
                    v['transcript'] = f"Video title: {v['title']}\nDescription: {v['description']}"
                    print(f'    ✗ Using title/description only')

    usable = [v for v in videos if v.get('transcript')]

    mode = detect_article_mode(topic)
    print(f'\n Generating article via {provider} from {len(usable)} video(s)... [mode: {mode}]')
    article_js = generate_article(topic, usable, provider=provider, mode=mode)

    if auto:
        # commit=True when --push (local automation), commit=False for GitHub Actions
        return publish_article(article_js, commit=push, push=push)

    while True:
        choice = show_preview(topic, article_js, videos)

        if choice == 'y':
            success = publish_article(article_js, push=push)
            return success

        elif choice == 'n':
            print('Skipped.')
            return False

        elif choice == 's':
            save_draft(topic, article_js)
            return False

        elif choice == 'r':
            print('\nRegenerating...')
            article_js = generate_article(topic, usable, provider=provider, mode=mode)

        else:
            print('Invalid choice. Enter y / n / s / r')


def main():
    args = sys.argv[1:]

    # Parse --ai <provider> flag
    provider = 'gemini'
    if '--ai' in args:
        idx = args.index('--ai')
        if idx + 1 < len(args):
            provider = args[idx + 1]
            args = [a for i, a in enumerate(args) if i != idx and i != idx + 1]

    # Parse --auto flag (non-interactive, used by GitHub Actions)
    auto = '--auto' in args
    if auto:
        args = [a for a in args if a != '--auto']

    # Parse --push flag (auto-push to GitHub after commit, local use)
    push = '--push' in args
    if push:
        args = [a for a in args if a != '--push']

    print(f'AI provider: {provider}{"  [AUTO]" if auto else ""}{"  [PUSH]" if push else ""}')

    # --publish path/to/draft.js
    if args and args[0] == '--publish':
        if len(args) < 2:
            print('Usage: python seo_youtube_pipeline.py --publish path/to/draft.js')
            sys.exit(1)
        publish_from_draft(args[1])
        return

    # --trending  (discover topic from YouTube trending videos)
    if args and args[0] == '--trending':
        print('Discovering trending topic from YouTube...')
        topic = get_trending_topic()
        if not topic:
            print('Could not discover trending topic. Falling back to queue.')
            topic = get_next_topic_from_queue()

        # Relevance check — reject topics not genuinely about Tally workflows
        print(f'Checking relevance of "{topic}"...')
        is_relevant, reason = check_topic_relevance(topic)
        if not is_relevant:
            print(f'  SKIP (not Tally-related): {reason}')
            mark_topic_done(topic)  # avoid picking same topic again
            print('  Trying queue fallback...')
            topic = get_next_topic_from_queue()
            print(f'  Queue topic: "{topic}"')

        print(f'Topic: "{topic}"')
        success = run_pipeline(topic, provider=provider, auto=auto, push=push)
        if success:
            mark_topic_done(topic)
        return

    # --queue  (pick next topic from static queue file)
    if args and args[0] == '--queue':
        topic = get_next_topic_from_queue()
        print(f'Queue topic: "{topic}"')
        success = run_pipeline(topic, provider=provider, auto=auto, push=push)
        if success:
            mark_topic_done(topic)
        return

    # Direct topic from command line
    if args:
        topic = ' '.join(args)
    else:
        topic = input('Enter topic: ').strip()

    if not topic:
        print('No topic provided.')
        sys.exit(1)

    run_pipeline(topic, provider=provider, auto=auto, push=push)


if __name__ == '__main__':
    main()
