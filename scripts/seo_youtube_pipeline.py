def run_pipeline(topic, provider='gemini', auto=False):
    """
    auto=True  — no interactive prompt, just write files (used by GitHub Actions).
                 Caller is responsible for committing/PR after this returns True.
    auto=False — interactive y/n/s/r approval (local use).
    """
    print(f'\n🔍 Searching YouTube: "{topic} tally india" ...')
    videos = search_videos(topic, max_results=6)

    if not videos:
        print('No videos found. Try a different topic.')
        return False

    print(f'Found {len(videos)} videos. Fetching transcripts...')
    for v in videos:
        v['transcript'] = get_transcript(v['id'])
        icon = '✓' if v['transcript'] else '✗'
        print(f'  {icon} {v["title"][:65]}')

    usable = [v for v in videos if v['transcript']]
    if not usable:
        # Stage 3: Gemini reads each YouTube video directly (handles Hindi, no captions needed)
        print('No captions found. Trying Gemini direct video analysis...')
        for v in videos[:1]:  # 1 video — keeps per-minute token usage low for article generation
            print(f'  Analyzing: {v["title"][:60]}...')
            content = get_content_via_gemini_video(v['id'], v['title'])
            if content:
                v['transcript'] = content
                print(f'    ✓ {len(content)} chars extracted by Gemini')
            else:
                # Stage 4: Last resort — title + description only
                v['transcript'] = f"Video title: {v['title']}\nDescription: {v['description']}"
                print(f'    ✗ Using title/description only')
        usable = [v for v in videos if v.get('transcript')]

    print(f'\n✍️  Generating article via {provider} from {len(usable)} video(s)...')
    article_js = generate_article(topic, usable, provider=provider)

    if auto:
        # Non-interactive: write files, skip git commit (GitHub Actions handles that)
        return publish_article(article_js, commit=False)

    while True:
        choice = show_preview(topic, article_js, videos)

        if choice == 'y':
            success = publish_article(article_js)
            return success

        elif choice == 'n':
            print('Skipped.')
            return False

        elif choice == 's':
            save_draft(topic, article_js)
            return False

        elif choice == 'r':
            print('\n♻️  Regenerating...')
            article_js = generate_article(topic, usable, provider=provider)

        else:
            print('Invalid choice. Enter y / n / s / r')


def main():
    args = sys.argv[1:]

    # Parse --ai <provider> flag
    provider = 'gemini'
    if '--ai' in args:
        idx = args.index('--ai')
        if idx + 1 < len(args):
            provider = args[idx + 1]
            args = [a for i, a in enumerate(args) if i != idx and i != idx + 1]

    # Parse --auto flag (non-interactive, used by GitHub Actions)
    auto = '--auto' in args
    if auto:
        args = [a for a in args if a != '--auto']

    print(f'AI provider: {provider}{"  [AUTO MODE]" if auto else ""}')

    # --publish path/to/draft.js
    if args and args[0] == '--publish':
        if len(args) < 2:
            print('Usage: python seo_youtube_pipeline.py --publish path/to/draft.js')
            sys.exit(1)
        publish_from_draft(args[1])
        return

    # --queue  (pick next topic from queue file)
    if args and args[0] == '--queue':
        topic = get_next_topic_from_queue()
        print(f'📋 Next topic from queue: "{topic}"')
        success = run_pipeline(topic, provider=provider, auto=auto)
        if success:
            mark_topic_done(topic)
        return

    # Direct topic from command line
    if args:
        topic = ' '.join(args)
    else:
        topic = input('Enter topic: ').strip()

    if not topic:
        print('No topic provided.')
        sys.exit(1)

    run_pipeline(topic, provider=provider, auto=auto)


if __name__ == '__main__':
    main()
