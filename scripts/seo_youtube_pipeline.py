def show_preview(topic, article_js, videos):
    print('\n' + '='*60)
    print(f'TOPIC: {topic}')
    print('RESEARCHED FROM:')
    for v in videos:
        icon = '✓' if v['transcript'] else '✗'
        print(f'  {icon} {v["title"][:70]}')
    print('='*60)
    print('\nGENERATED ARTICLE (preview):')
    print('-'*60)

    # Extract title and slug for quick review
    slug_match = re.search(r"slug:\s*'([^']+)'", article_js)
    title_match = re.search(r"title:\s*'([^']+)'", article_js)
    desc_match = re.search(r"description:\s*'([^']+)'", article_js)

    if slug_match:
        print(f'  URL:   /blog/{slug_match.group(1)}')
    if title_match:
        print(f'  Title: {title_match.group(1)}')
    if desc_match:
        print(f'  Desc:  {desc_match.group(1)[:100]}')

    print('\n--- FULL CONTENT ---')
    # Show first 1500 chars
    preview = article_js[:1500]
    print(preview)
    if len(article_js) > 1500:
        print(f'\n  ... ({len(article_js) - 1500} more chars) ...')
    print('-'*60)

    print('\nOptions:')
    print('  y      — Approve and publish to develop branch')
    print('  n      — Skip this topic')
    print('  s      — Save draft for manual editing')
    print('  r      — Regenerate article')

    return input('\nYour choice (y/n/s/r): ').strip().lower()


# ─────────────────────────────────────────────
# STEP 5 — PUBLISH
# ─────────────────────────────────────────────
def publish_article(article_js, commit=True):
    """Write article to all files. commit=False skips git (GitHub Actions commits separately)."""
    slug_match = re.search(r"slug:\s*'([^']+)'", article_js)
    if not slug_match:
        print('ERROR: Cannot find slug in article. Save as draft and check manually.')
        return False

    slug = slug_match.group(1)
    print(f'\nWriting /blog/{slug} to files...')

    # ── blogData.js ──
    blog_data = BLOG_DATA_FILE.read_text(encoding='utf-8')
    if blog_data.rstrip().endswith('];'):
        new_blog_data = blog_data.rstrip()[:-2].rstrip()
        if not new_blog_data.rstrip().endswith(','):
            new_blog_data += ','
        new_blog_data += f'\n  {article_js}\n];\n'
        BLOG_DATA_FILE.write_text(new_blog_data, encoding='utf-8')
        print('  ✓ blogData.js updated')
    else:
        print('  ERROR: blogData.js format unexpected. Save as draft.')
        return False

    # sitemap.js auto-generates from blogData.js — no manual update needed

    if not commit:
        print(f'  ✓ Files written. Slug: {slug}')
        # Write slug to a file so the GitHub Actions step can read it
        (REPO_ROOT / '.seo_last_slug').write_text(slug, encoding='utf-8')
        return True

    # ── git commit (local use only) ──
    files = [
        str(BLOG_DATA_FILE),
    ]
    subprocess.run(['git', 'add'] + files, cwd=REPO_ROOT, check=True)
    commit_msg = f'feat(seo): add {slug} article via YouTube pipeline'
    result = subprocess.run(
        ['git', 'commit', '-m', commit_msg],
        cwd=REPO_ROOT, capture_output=True, text=True
    )
    if result.returncode == 0:
        print(f'  ✓ Committed: {commit_msg}')
    else:
        print(f'  WARNING: git commit issue: {result.stderr}')

    print(f'\n✅ Done! /blog/{slug} is ready.')
    print('Next step: git push origin develop')
    print('Then merge develop → main to go live on Netlify.')
    return True


