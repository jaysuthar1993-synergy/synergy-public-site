def generate_article(topic, videos_with_transcript, provider='gemini', mode=None):
    if mode is None:
        mode = detect_article_mode(topic)
    print(f'  Article mode: {mode}')
    prompt = build_prompt(topic, videos_with_transcript, mode=mode)

    if provider == 'gemini':
        return _generate_gemini(prompt)
    elif provider == 'openai':
        return _generate_openai(prompt)
    elif provider == 'claude':
        return _generate_claude(prompt)
    else:
        print(f'Unknown provider: {provider}. Using gemini.')
        return _generate_gemini(prompt)


def _generate_gemini(prompt):
    """FREE — Gemini 1.5 Flash. 1,500 requests/day free tier. Get key: aistudio.google.com"""
    try:
        from google import genai
        from google.genai import types
    except ImportError:
        print("ERROR: Run: pip install google-genai")
        sys.exit(1)

    if not GEMINI_API_KEY:
        print("ERROR: Set GEMINI_API_KEY — get free key at aistudio.google.com (no credit card)")
        sys.exit(1)

    client = genai.Client(api_key=GEMINI_API_KEY)
    for attempt in range(3):
        try:
            response = client.models.generate_content(
                model='gemini-2.5-flash-lite',
                contents=prompt,
                config=types.GenerateContentConfig(max_output_tokens=4000, temperature=0.7)
            )
            return _clean_article(response.text.strip())
        except Exception as e:
            err = str(e)
            if ('429' in err or '503' in err) and attempt < 2:
                wait = 40 + attempt * 20
                print(f'  Gemini busy ({err[:60]}) — waiting {wait}s before retry ({attempt+1}/3)...')
                time.sleep(wait)
            else:
                print(f'ERROR: Gemini generation failed: {e}')
                sys.exit(1)


def _clean_article(text):
    """Strip markdown code fences Gemini sometimes wraps output in."""
    text = text.strip()
    # Remove ```json ... ``` or ``` ... ``` wrappers
    if text.startswith('```'):
        text = re.sub(r'^```[a-z]*\n?', '', text)
        text = re.sub(r'\n?```$', '', text)
        text = text.strip()
    return text


def _generate_openai(prompt):
    """~₹0.20/article — GPT-4o-mini"""
    try:
        from openai import OpenAI
    except ImportError:
        print("ERROR: Run: pip install openai")
        sys.exit(1)

    if not OPENAI_API_KEY:
        print("ERROR: Set OPENAI_API_KEY — get key at platform.openai.com")
        sys.exit(1)

    client = OpenAI(api_key=OPENAI_API_KEY)
    resp = client.chat.completions.create(
        model='gpt-4o-mini',
        max_tokens=4000,
        messages=[{'role': 'user', 'content': prompt}]
    )
    return resp.choices[0].message.content.strip()


def _generate_claude(prompt):
    """~₹4.50/article — Claude claude-sonnet-4-6 (best quality, most expensive)"""
    try:
        import anthropic
    except ImportError:
        print("ERROR: Run: pip install anthropic")
        sys.exit(1)

    if not ANTHROPIC_API_KEY:
        print("ERROR: Set ANTHROPIC_API_KEY — get key at console.anthropic.com")
        sys.exit(1)

    client = anthropic.Anthropic(api_key=ANTHROPIC_API_KEY)
    message = client.messages.create(
        model='claude-sonnet-4-6',
        max_tokens=4000,
        messages=[{'role': 'user', 'content': prompt}]
    )
    return message.content[0].text.strip()


