def search_videos(topic, max_results=5):
    try:
        from googleapiclient.discovery import build
    except ImportError:
        print("ERROR: Run: pip install google-api-python-client")
        sys.exit(1)

    if not YOUTUBE_API_KEY:
        print("ERROR: Set YOUTUBE_API_KEY environment variable")
        sys.exit(1)

    youtube = build('youtube', 'v3', developerKey=YOUTUBE_API_KEY)
    search_query = f"{topic} tally india"
    request = youtube.search().list(
        part='snippet',
        q=search_query,
        type='video',
        maxResults=max_results,
        relevanceLanguage='en',
        order='relevance',
        regionCode='IN'
    )
    response = request.execute()

    videos = []
    for item in response.get('items', []):
        vid_id = item['id'].get('videoId')
        if vid_id:
            videos.append({
                'id': vid_id,
                'title': item['snippet']['title'],
                'channel': item['snippet']['channelTitle'],
                'description': item['snippet']['description'][:300],
                'url': f'https://youtube.com/watch?v={vid_id}',
                'transcript': None
            })
    return videos


