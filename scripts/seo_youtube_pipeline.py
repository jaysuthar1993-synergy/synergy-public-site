def generate_article(topic, videos_with_transcript, provider='gemini'):
    prompt = build_prompt(topic, videos_with_transcript)

    if provider == 'gemini':
        return _generate_gemini(prompt)
    elif provider == 'openai':
        return _generate_openai(prompt)
    elif provider == 'claude':
        return _generate_claude(prompt)
    else:
        print(f'Unknown provider: {provider}. Using gemini.')
        return _generate_gemini(prompt)


def _generate_gemini(prompt):
    """FREE — Gemini 1.5 Flash. 1,500 requests/day free tier. Get key: aistudio.google.com"""
    try:
        from google import genai
        from google.genai import types
    except ImportError:
        print("ERROR: Run: pip install google-genai")
        sys.exit(1)

    if not GEMINI_API_KEY:
        print("ERROR: Set GEMINI_API_KEY — get free key at aistudio.google.com (no credit card)")
        sys.exit(1)

    client = genai.Client(api_key=GEMINI_API_KEY)
    for attempt in range(3):
        try:
            response = client.models.generate_content(
                model='gemini-2.5-flash-lite',
                contents=prompt,
                config=types.GenerateContentConfig(max_output_tokens=4000, temperature=0.7)
            )
            return response.text.strip()
        except Exception as e:
            err = str(e)
            if ('429' in err or '503' in err) and attempt < 2:
                wait = 40 + attempt * 20
                print(f'  Gemini busy ({err[:60]}) — waiting {wait}s before retry ({attempt+1}/3)...')
                time.sleep(wait)
            else:
                print(f'ERROR: Gemini generation failed: {e}')
                sys.exit(1)


def _generate_openai(prompt):
    """~₹0.20/article — GPT-4o-mini"""
    try:
        from openai import OpenAI
    except ImportError:
        print("ERROR: Run: pip install openai")
        sys.exit(1)

    if not OPENAI_API_KEY:
        print("ERROR: Set OPENAI_API_KEY — get key at platform.openai.com")
        sys.exit(1)

    client = OpenAI(api_key=OPENAI_API_KEY)
    resp = client.chat.completions.create(
        model='gpt-4o-mini',
        max_tokens=4000,
        messages=[{'role': 'user', 'content': prompt}]
    )
    return resp.choices[0].message.content.strip()


