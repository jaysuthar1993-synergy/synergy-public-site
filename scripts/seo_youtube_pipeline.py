def save_draft(topic, article_js):
    DRAFTS_DIR.mkdir(exist_ok=True)
    safe_topic = re.sub(r'[^a-z0-9]+', '-', topic.lower()).strip('-')
    draft_path = DRAFTS_DIR / f'draft-{safe_topic}.js'
    draft_path.write_text(article_js, encoding='utf-8')
    print(f'\nDraft saved: {draft_path}')
    print('Edit it, then run:')
    print(f'  python scripts/seo_youtube_pipeline.py --publish "{draft_path}"')


def publish_from_draft(draft_path):
    path = Path(draft_path)
    if not path.exists():
        print(f'ERROR: Draft not found: {draft_path}')
        sys.exit(1)
    article_js = path.read_text(encoding='utf-8').strip()
    success = publish_article(article_js)
    if success:
        path.unlink()
        print('Draft file removed after publish.')


# ─────────────────────────────────────────────
# TOPIC QUEUE (for daily/recurring use)
# ─────────────────────────────────────────────
def get_next_topic_from_queue():
    if not QUEUE_FILE.exists():
        print(f'No queue file found at {QUEUE_FILE}')
        print('Create it with one topic per line.')
        sys.exit(1)

    done = set()
    if DONE_FILE.exists():
        done = set(DONE_FILE.read_text(encoding='utf-8').splitlines())

    topics = [
        line.strip()
        for line in QUEUE_FILE.read_text(encoding='utf-8').splitlines()
        if line.strip() and not line.startswith('#') and line.strip() not in done
    ]

    if not topics:
        print('All topics in queue have been processed.')
        print(f'Add more topics to {QUEUE_FILE}')
        sys.exit(0)

    return topics[0]


def mark_topic_done(topic):
    with open(DONE_FILE, 'a', encoding='utf-8') as f:
        f.write(topic + '\n')


