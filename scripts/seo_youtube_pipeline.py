def get_next_topic_from_queue():
    if not QUEUE_FILE.exists():
        print(f'No queue file found at {QUEUE_FILE}')
        print('Create it with one topic per line.')
        sys.exit(1)

    done = set()
    if DONE_FILE.exists():
        done = set(DONE_FILE.read_text(encoding='utf-8').splitlines())

    topics = [
        line.strip()
        for line in QUEUE_FILE.read_text(encoding='utf-8').splitlines()
        if line.strip() and not line.startswith('#') and line.strip() not in done
    ]

    if not topics:
        print('All topics in queue have been processed.')
        print(f'Add more topics to {QUEUE_FILE}')
        sys.exit(0)

    return topics[0]


def mark_topic_done(topic):
    with open(DONE_FILE, 'a', encoding='utf-8') as f:
        f.write(topic + '\n')


