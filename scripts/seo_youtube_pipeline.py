def save_draft(topic, article_js):
    DRAFTS_DIR.mkdir(exist_ok=True)
    safe_topic = re.sub(r'[^a-z0-9]+', '-', topic.lower()).strip('-')
    draft_path = DRAFTS_DIR / f'draft-{safe_topic}.js'
    draft_path.write_text(article_js, encoding='utf-8')
    print(f'\nDraft saved: {draft_path}')
    print('Edit it, then run:')
    print(f'  python scripts/seo_youtube_pipeline.py --publish "{draft_path}"')


def publish_from_draft(draft_path):
    path = Path(draft_path)
    if not path.exists():
        print(f'ERROR: Draft not found: {draft_path}')
        sys.exit(1)
    article_js = path.read_text(encoding='utf-8').strip()
    success = publish_article(article_js)
    if success:
        path.unlink()
        print('Draft file removed after publish.')


