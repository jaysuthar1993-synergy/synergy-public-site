export function getRecentUpdates(limit = 20) {
  return updates
    .slice()
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);
}

export function getUpdatesByType(type) {
  return updates.filter(u => u.type === type).sort((a, b) => new Date(b.date) - new Date(a.date));
}
